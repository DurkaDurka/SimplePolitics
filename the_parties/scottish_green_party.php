			<h3>Manifesto</h3>

                <p><u><strong>ECONOMY</strong></u></p>

                <ul>
                	<p><strong>A More Equal Socitey:</strong></p>

                	<p>As well as increasing the minimum wage to the level of a living wage, Greens believe there should be maximum wage ratios in the public sector and, ultimately, throughout the private sector. Greens would support better parental leave for all parents, protect the interests of older people by investing in housing and providing a Citizens’ Pension at a genuinely liveable level, and would use the benefits system to recognise the role of volunteers and carers in our society.</p>
                </ul>

                <button id="expander">Show More</button>

				<div id="expanded" style="display: none;"><br>

                <ul>
                	<p><strong>Financial Services:</strong></p>

                	<p> Greens believe that the immense power wielded by these banks should be curbed. Instead we should be encouraging and supporting mutual financial institutions such as building societies and co-operatives. Banks that have been bailed out with public funds should be used for public good – investing in small businesses, low-carbon technologies and ethical trade, rather than supporting the arms industry and dirty projects like tar sands.</p>
                </ul>

                <ul>
                	<p><strong>Enterprise:</strong></p>

                	<p>Current government policy fails to protect independent local businesses, and instead favours large multinationals. In contrast, Greens would place a greater emphasis on local economies and small businesses. Reforming public sector procurement policies to let these companies into the process would be a straightforward way to support smaller businesses and local companies.</p>
                </ul>

                <p><u><strong>EDUCATION</strong></u></p>

                <ul>
                	<p>Greening education is not just about the curriculum, but also about where teaching takes place and how schools function. Outdoor education develops skills and qualities such as risk assessment, working with others, self-confidence, empathy with Scotland’s countryside, co-ordination and practical skills. The reduction in opportunities to experience outdoor education is unacceptable.</p>
                </ul>

                <p><u><strong>ENERGY</strong></u></p>

                <ul>
                	<p><strong>Cutting Consumption:</strong></p>

                	<p>Allowing energy consumption to rise year on year will make it impossible to meet climate targets, and will make costs unbearable for households and businesses alike. Poor standards of energy efficiency in much of Scotland’s housing stock are the cause of considerable fuel poverty as well as high carbon emissions. Better standards for new homes will help, but only an ambitious programme to insulate all existing housing in Scotland will achieve the progress needed, addressing climate change, poverty and energy security.</p>
                </ul>

                <ul>
                	<p><strong>Stop trying to build are way out of congestion:</strong></p>

                	<p>Every party which has run government in Scotland to date has failed to break the addiction to road-building, even when it’s clear that it just makes the problem worse. Moving to electric vehicles will help reduce emissions directly, but unless we get a grip on traffic levels the energy demand will be astronomical. We need to plan where homes and businesses are build and where public services are delivered, to reduce people’s need to drive ever further. Walking, cycling and public transport must be given far higher priority by Government, especially when it comes to public spending.</p>
                </ul>

                <ul>
                	<p><strong>Serious about Renewables:</strong></p>

                	<p>Scotland is home to the biggest renewable energy resource in Europe, but the Scottish Government has been slow to approve major renewable projects and grid upgrade, and has done next to nothing to promote local renewables at community level. Support for developing marine energy has been cut. This must change if we’re to have a chance to turn this renewable potential into reality.</p>
                </ul>

                <ul>
                	<p><strong>Nuclear and Coal:</strong></p>

                	<p>With carbon capture and storage, coal might have a continued role to play in the short term. But we don’t yet know whether CCS is technically feasible or commercially viable, and it’s far too much of a gamble. New coal and nuclear plants are not the answer to energy security, climate change or peak oil.</p>
                </ul>

                <p><u><strong>ENVIRONMENT</strong></u></p>

                <ul>
                	<p><strong>Reconnecting people with Land:</strong></p>

                	<p>Scotland’s land reform agenda has begun to address some of the historical inequalities of land distribution. Using the modest land reform legislation of recent years, some rural communities, particularly in the Highlands and Islands, have bought their land and set inspiring examples of regeneration. We want to keep up this momentum and extend the land reform legislation to create new opportunities for urban and rural communities.</p>
                </ul>

                <ul>
                	<p><strong>Food Farming:</strong></p>

                	<p>Greens support well-regulated, sustainable aquaculture and fisheries. We acknowledge the sacrifices made in recent years by fishermen and their communities and recognise that there are signs of recovery which should not be undermined. We will put in place a fishing community regeneration plan with the health of fish stocks at its heart, redirecting fisheries subsidies towards recovery and putting in place a long-term fisheries management policy to revive stocks and secure fishing communities.</p>
                </ul>

                <ul>
                	<p><strong>Ending Animal Cruelty:</strong></p>

                	<p>Greens want to build a society that cares for its environment and the natural world that supports it. Failure to respect the environment reduces our quality of life, harms livelihoods and cheats future generations. We support sensitive stewardship of our natural resources, to leave our children’s children a legacy of clean air, pure water, fertile farmland, abundant forests, vibrant urban green spaces and plentiful wildlife.</p>
                </ul>

                <p><u><strong>HEALTH</strong></u></p>

                <ul>
                	<p>Sport and physical activity make an important contribution to health and happiness. However public sports facilities and green spaces have been lost all over the country, and many are still under threat. This erosion must end, and Scotland’s recreational spaces should be given much stronger protection, especially green spaces and playing fields, which provide a vital resource for spontaneous and informal sporting activities.</p>
                </ul>

                <p><u><strong>INTERNATIONAL</strong></u></p>

                <ul>
                	<p><strong>Europe:</strong></p>

                	<p>Greens want to see strong self-reliant local and regional economies in Europe, instead of pushing for ever greater centralisation. In a more democratic and accountable Europe, power should be placed in the hands of citizens through a stronger Parliament, and greater use of referendums.</p>
                </ul>

                <ul>
                	<p><strong>Trident:</strong></p>

                	<p>We believe that the possession of weapons of mass destruction is wrong at any time. But the idea of committing tens of billions of pounds to replace Trident at a time when there are real threats to the funding of public services is particularly repulsive. Greens campaign against Trident both inside Parliament, where we won the first ever clear vote against it at Holyrood, and by participating alongside others in the peaceful direct action which has kept this issue on the political agenda in Scotland over the years.</p>
                </ul>

                <ul>
                	<p><strong>Arms Trade:</strong></p>

                	<p>The UK has a long history of profiteering from the arms trade, and has utterly failed to give any meaning to the phrase “ethical foreign policy”. Ending government support for arms fairs, banning the export of offensive weapons and regulating private arms brokers and security consultants would take us in the right direction.</p>
                </ul>

                <ul>
                	<p><strong>Uk Foreign Policy:</strong></p>

                	<p>By participating the illegal and immoral invasion of Iraq, the UK made the world a more dangerous place and further undermined the institutions which are supposed to bring nations together. The UN Security Council remains dominated by the planet’s biggest arms dealers, including the UK and US Governments, and must be reformed to ensure fair representation of all the world’s people. The UN itself must have the funding and the authority to mediate in conflicts, which will increasingly occur over access to basic resources as a result of climate change.</p>
                </ul>

                <ul>
                	<p><strong>Global Justice and Eradication of Policy:</strong></p>

                	<p>The growth of ethical and fair trade has shown that many people want an alternative which places human dignity above profit, and which tackles poverty and injustice. It’s time to make all trade fair trade, by reforming the global rules and reining in the power of the multinationals.</p>
                </ul>

                <p><u><strong>JUSTICE</strong></u></p>

                <ul>
                	<p>We are deeply concerned about the power being taken by government and the private sector over people’s online activity, and we believe that a new approach is needed to issues like copyright, patents, file-sharing and the protection of personal data. Government should put the interests of citizens first, not serving the commercial interests of big business. Many of our members are advocates of the Free Software Movement, and we believe that the principles which underpin it have wider application in society.</p>
                </ul>

                <p><u><strong>SOCIETY</strong></u></p>

                <ul>
                	<p><strong>Housing:</strong></p>

                	<p>We believe that local communities also need to have more control over the decisions which affect them, such as housing and development. Community ownership of housing, including through co-operatives and housing associations, can create a real sense of the common good among people. Small community-led organisations can also bring real creativity to challenges like local energy schemes, crime prevention and public health.</p>
                </ul>

                <ul>
                	<p><strong>Equality:</strong></p>

                	<p>Scotland can be proud of progress that has been made in enhancing equality and protecting diversity. However we still have not succeeded in creating a country where all can participate fully in society regardless of race, gender, disability, age, sexual orientation and religion or belief. Tackling discrimination in society doesn’t just benefit the individuals concerned, it also benefits our whole society. We believe equality is an important measure of Scotland’s progress.</p>
                </ul>

                <ul>
                	<p><strong>Democracy:</strong></p>

                	<p>Greens believe in radical democracy, and in devolution down beyond Holyrood as well as greater powers for the Scottish Parliament. The current debate over the constitution boils down to this: the other parties want to see the same old models persist, they just disagree where they should be run from.</p>
                </ul>

