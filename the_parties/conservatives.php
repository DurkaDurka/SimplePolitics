<ul class="bulletList" ><li>Stop tax credits for families with incomes over &pound;50,000, cut government contributions to Child Trust Funds for all but the poorest third of families and families with disabled children </li><li>More support for employers to encourage auto-enrolment into pensions, while addressing &quot;growing disparity&quot; between private and public sector pensions. Cap public sector pensions above &pound;50,000 </li><li>Raise inheritance tax threshold to &pound;1m </li><li>Launch a free national financial advice service, funded by a new &quot;social responsibility levy&quot; on financial services </li><li>No-one to be forced to sell their home to pay unsecured debts of less than &pound;25,000 </li><li>Re-link the basic state pension to earnings and protect the winter fuel payment, free bus passes, free TV licences, disability living and attendance allowances and pension credit. </li></ul><p><b>Big difference from Labour:</b></p><p><i>The Conservatives' pledge to raise the inheritance tax threshold, which Labour have frozen.</i></p><p></p>
    
