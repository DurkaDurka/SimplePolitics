<p>The manifesto does not specify whether these policies apply across the UK or will be devolved. </p><ul class="bulletList" ><li>Raise entry requirement for taxpayer-funded primary school teacher training, with new graduates having at least a 2:2 degree [England and Wales only] </li><li>Give state schools freedom to offer international exams used by private schools and all pupils to have chance to study single sciences [England only] </li><li>Create 20,000 additional young apprenticeships and allow schools to offer workplace training </li><li>Parents to get power to save schools threatened by closure, allowing communities the chance to take over and run good small schools [England only] </li><li>Failing schools to be inspected more often - with the best schools visited less frequently [England only] </li><li>Provide 10,000 extra university places this year, paid for by giving graduates incentives to pay back their student loans [England and Wales only] </li><li>Extend right to request flexible working but in the longer term, after consultation with business [Except Northern Ireland] </li><li>Flexible parental leave which lets parents share maternity leave between them [Except Northern Ireland] </li><li>Increased use of mediation when couples break up and review access rights for non-resident parents and grandparents </li><li>Clamp down on marketing aimed at children </li><li>Provide 4,200 more Sure Start health visitors, with Sure Start providers pay partly based on results, with Early Years Support Team to look after early intervention and parenting support [England only] </li></ul><p><b>Big difference from Labour:</b></p><p><i>No extensions to free nursery care, tax credits for families with toddlers or paid paternity leave, with the Tories instead focusing on improving school standards through parent power and better teaching.</i></p><p></p>
    
	<div class="ch1"><b>ON HEALTH</b></div>
    



<p>As with education, the manifesto does not specify whether these policies apply across the UK or will be devolved. </p><ul class="bulletList" ><li>Power for patients to choose any healthcare provider that meets NHS standards, within NHS prices [England only] </li><li>Decentralise power, getting rid of target-led culture and publishing healthcare providers' results. Introducing payment by results system for GPs and other health services [England only] </li><li>Patients put in charge of decisions about their care, including control of their health records [England only] </li><li>Doctors and nurses to get more decision-making powers. Independent NHS board to allocate resources and provide commissioning guidelines. Power for GPs to commission local health services [England only] </li><li>More single rooms in hospitals, an end to mixed-sex wards and an end to &quot;forced closure&quot; of accident and emergency wards [England only] </li><li>Cancer drug fund to give more access to medication for rare cancers and more clinical trials encouraged [England only] </li><li>No compulsory levy to pay for social care. More support for older people to live independently at home and voluntary insurance to protect home from being sold to pay for residential care [England only] </li><li>Public health funding directed to poorest areas with the worst health outcomes [England only] </li></ul><p><b>Big difference from Labour:</b></p><p><i>Fundamental disagreement over social care for the elderly. The Conservatives prefer voluntary insurance schemes to Labour's National Care Service, funded by a compulsory levy</i></p><p></p>
    
