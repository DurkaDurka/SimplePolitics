<ul class="bulletList" ><li>Reduce the number of MPs by 10% and cut ministers' pay by 5%, followed by a five-year freeze </li><li>All items of spending over &pound;25,000 published online, along with salaries of senior civil servants in central government </li><li>Clear financial performance targets for senior civil servants </li><li>Ban ex-ministers from lobbying government for two years after leaving office </li><li>Stop central government bodies using public money to hire people to lobby other government bodies </li><li>Any former minister found to have broken rules on appointments forced to give up some or all of their ministerial pension </li><li>More power for backbench MPs, more free votes and use of government Royal Prerogative subject to greater democratic control so that Parliament is properly involved in all big national decisions </li></ul><p><b>Big difference from Labour:</b></p><p><i>Action from both on lobbying but Labour aspire to an elected upper house and alternative voting methods, while the Tories want to cut the number of MPs and focus on their pay.</i></p><p></p>
    
