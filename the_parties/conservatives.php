<ul class="bulletList" ><li>Councils and police to get powers to shut shops or bars persistently selling alcohol to children and to charge more for late-night licences to cover policing costs [England only] </li><li>Double the maximum fine for under-age alcohol sales to &pound;20,000 and ban off licences and supermarkets from selling alcohol below cost price </li><li>Social enterprises, charities and voluntary groups to deliver more public services aimed at tackling deep-rooted social problems </li><li>Education, mentoring and drug rehabilitation programmes to help young offenders &quot;go straight&quot; </li><li>Annual limit on the number of non-EU economic migrants to UK, with access only for those who bring &quot;most value to British economy&quot; </li><li>Foreign students to pay bond on entering the country, to be repaid when they leave </li></ul><p><b>Big difference from Labour:</b></p><p><i>Disagreement over immigration, with the Conservatives supporting a points system but with an annual limit, while Labour prefer the points-based system with no cap.</i></p><p></p>
    
